// Estrutura de Repetição - Switch Case - Troca

let airport = 'SKU'

switch (airport) {
    case 'MCO':
        console.log('Orlando')
        break
    case 'JFK':
        console.log('John F. Kennedy')
        break
    case 'SEA':
        console.log('Seatle')
        break
    default:
        console.log('Unknow')
}